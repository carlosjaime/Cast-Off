World.prototype.removeClient = function(client) {
    for (var i = 0; i < this.clients.length; i++) {
        if (this.clients[i].sessionId == client.sessionId) {
            this.clients.splice(i, 1);
            console.log("client removed", this.clients.length);
            break;
        }
    }        
    console.log("no client found to remove with sID", client.sessionId);
};

World.prototype.getOtherEntities = function(client) {
    // for now this only caters for clients...
    var others = [];
    for (var i = 0; i < this.clients.length; i++) {
        if (this.clients[i].sessionId != client.sessionId) {
            others.push(this.clients[i].getPosition());
        }
    }
    return others;
};

